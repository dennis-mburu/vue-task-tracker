<script>
export default {
  props: {
    task: Object,
  },
  methods: {
    onDelete(id) {
      // this.$emit("deleteTask", id);
      this.$emit("delete-task", id)
    },
  },
};
</script>

<template>
  <div :class="[task.reminder ? 'reminder' : '', 'task']">
    <h3>
      {{ task.text }}
      <i @click="onDelete(task.id)" class="fas fa-times"></i>
    </h3>
    <p>{{ task.day }}</p>
  </div>
</template>

